//In this file, we will learn about Upper and Lower case method.

//To convert the lower to upper case, we have to use toUpperCase(),
let firstName = 'vijaygopal'; //the string is in lower case.
firstName = firstName.toUpperCase(); //in this step, the lower case string converted to upper case and stored in a variable firstName.
console.log(firstName); //the output will in UpperCase.

//the same applies for lowercase.
let lastName = 'BALASA';
lastName = lastName.toLowerCase();//in this step, the upper case string converted to lower case.
console.log(lastName); //the output will be in lower case.
