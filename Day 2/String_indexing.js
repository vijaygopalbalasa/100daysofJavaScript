//String indexing

/*
1. Indexing starts from 0 to n-1
2. length of the string starts from 1.
*/

var firstName = 'vijaygopal';
console.log(firstName[3]); // this will print the character at index 3 which is 'a'.

console.log(firstName.length); // this will print the length of the string which starts from 1.

console.log(firstName[firstName.length - 1]); // this will give the last index of the string. 
