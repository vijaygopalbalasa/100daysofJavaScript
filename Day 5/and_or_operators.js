//And, OR operators in looping condition.
//AND operator 
let age = 23;
let name = "vijay";

if (name[0] === 'v' && age > 23) {
    console.log("your name and age is matching");
} else {
    console.log("Incorrect details, recheck again");
}

//OR operator

if (name[0] === 'v' || age === 23) {
    console.log("your name and age is matching");
} else {
    console.log("Incorrect details, recheck again");
}